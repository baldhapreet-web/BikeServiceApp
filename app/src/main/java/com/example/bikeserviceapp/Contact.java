package com.example.bikeserviceapp;

public class Contact {

    public String phone;
    public String email;
    public String address;
    public String whatsapp;

    public Contact(){}

    public Contact(String phone,String email,String address,String whatsapp){
        this.phone = phone;
        this.email = email;
        this.address = address;
        this.whatsapp = whatsapp;
    }
}